package com.taobao.yinggang.curatorexample.zk;

import java.io.UnsupportedEncodingException;

/**
 * @author <a href="mailto:yinggang.zg@taobao.com">yinggang.zg</a>
 * @version 1.0
 * @since 2014年6月4日
 */
public class SerializeUtil {
	public static String deserialize(byte[] bytes) throws UnsupportedEncodingException {
		return null != bytes ? new String(bytes, "UTF-8") : null;
	}

	public static byte[] serialize(String data) throws UnsupportedEncodingException {
		return data.getBytes("UTF-8");
	}
}
